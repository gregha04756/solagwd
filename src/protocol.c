unsigned short int
calc_crc16(unsigned char *ptr, int len)
{
    unsigned char uchCRCHi = 0xFF;	// high CRC byte

    unsigned char uchCRCLo = 0xFF;	// low CRC byte

    unsigned uIndex;		// will index into CRC lookup

    while (len--) {		// pass through message buffer

	uIndex = uchCRCHi ^ *ptr++;	// calculate the CRC

	uchCRCHi = uchCRCLo ^ auchCRCHi[uIndex];
	uchCRCLo = auchCRCLo[uIndex];
    }

    return (uchCRCHi << 8 | uchCRCLo);

}

int
check_crc16(unsigned char *buf, int buflen)
{
	unsigned short int crc;
	unsigned char crcHI, crcLO;

	/*
	 * Calculate the actual CRC of the message
	 */
	crc = calc_crc16(buf, buflen-MBCRCSIZE);
	crcHI = (crc>>8) & 0xFF;
	crcLO = crc & 0xFF;


	/*
	 * Check this against the given CRC
	 */

	return buf[buflen-2] == crcHI && buf[buflen-1] == crcLO;

}
