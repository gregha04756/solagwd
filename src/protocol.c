int
buf2mbap(unsigned char *buf, int bufsize, mbap_t *mbapheader)
{
	if(bufsize < MBAPBUFSIZE) {
		if(debug) {
			fprintf(stderr, "buf2mbap: invalid bufsize\n");
		}
		return 0;
	}
	mbapheader->transaction_id = (buf[0]<<8) + buf[1];
	mbapheader->protocol_id = (buf[2]<<8) + buf[3];
	mbapheader->length = (buf[4]<<8) + buf[5];
	mbapheader->unit = buf[6];

	return 1;
}


int
mbap2buf(mbap_t *mbapheader, unsigned char *buf, int bufsize)
{
	if(bufsize < MBAPBUFSIZE) {
		if(debug) {
			fprintf(stderr, "mbap2buf: invalid bufsize %d\n", bufsize);
		}
		return 0;
	}
	buf[0] = (mbapheader->transaction_id >> 8) & 0xFF;
	buf[1] = mbapheader->transaction_id & 0xFF;

	buf[2] = (mbapheader->protocol_id >> 8) & 0xFF;
	buf[3] = mbapheader->protocol_id & 0xFF;

	buf[4] = (mbapheader->length >> 8) & 0xFF;
	buf[5] = mbapheader->length & 0xFF;

	buf[6] = mbapheader->unit;

	return 1;
}


/*
 * Given an mbap header and a source buffer for a modbus ip packet,
 * build a serial packet.  This means copying the address to the
 * beginning of the message, and adding the CRC.
 *
 * The result is passed back in destbuf
 */
int
build_serial_pdu(mbap_t *header, unsigned char *srcbuf, int srcbufsize,
	unsigned char *destbuf, int destbufsize)
{
	unsigned short int crc;
	int i;

	/*
	 * Make sure the source looks correct
	 */
	if(header->protocol_id != 0) {
		if(debug)
			fprintf(stderr, "Invalid protocol ID\n");
		return 0;
	}

	if((header->length-1 + MBADDRSIZE + MBCRCSIZE) > destbufsize) {
		if(debug)
			fprintf(stderr, "Buffer too short\n");
		return 0;
	}

	/*
	 * Copy the address
	 */
	destbuf[0] = header->unit;

	/*
	 * Copy the PDU
	 */
	for(i=0; i < header->length-1; i++)
		destbuf[i+1] = srcbuf[i];


	/*
	 * Create the crc
	 */
	crc = calc_crc16(destbuf, header->length);

	/*
	 * Append the CRC to the message
	 */
	destbuf[header->length] = (crc>>8) & 0xFF;
	destbuf[(header->length)+1] = crc & 0xFF;

	return header->length-1 + MBADDRSIZE + MBCRCSIZE;
}

/*
 * Build an IP PDU
 *
 * As input, take an mbap header and a serial modbus message.  The
 * CRC of the serial message is checked.
 *
 * The result is placed in destbuf and includes the mbap
 * header.
 */
int
build_ip_pdu(mbap_t *header,
	unsigned char *srcbuf,
	int srcbuflen,
	unsigned char *destbuf,
	int destbuflen)
{

	/*
	 * Will it fit?
	 */
	if(destbuflen < sizeof(*header) + srcbuflen - MBCRCSIZE) {
		if(debug) {
			fprintf(stderr, "build_ip_pdu(): destination buffer too small\n");
		}
		return 0;
	}

	/*
	 * Is its CRC good?
	 */
	if(check_crc16(srcbuf, srcbuflen)==0) {
		if(debug) {
			fprintf(stderr, "build_ip_pdu(): source message CRC is bad\n");
		}
		return 0;
	}

	/*
	 * Modify the mbap header length to indicate the actual
	 * length of the response
	 */
	header->length = srcbuflen-MBCRCSIZE;


	/*
	 * Copy the mbap into the buffer
	 */
	if(!mbap2buf(header, destbuf, destbuflen)) {
		return 0;
	}
	destbuf = destbuf + MBAPBUFSIZE;

	/*
	 * Copy the data, less the unit address (which is now in the
	 * header) and the CRC
	 */
	memcpy(destbuf, srcbuf+1, srcbuflen - 1 - MBCRCSIZE);

	return srcbuflen - MBADDRSIZE - MBCRCSIZE + MBAPBUFSIZE;
}


